Texture* getTextureAt(int x, int y)
{
    //printf("getting texture at (%d, %d)\n", x, y);
    if( (x >= 0 && x < MAP_WIDTH) && (y >= 0 && y < MAP_HEIGHT) )
        return getTexture(walls[y][x]-1);
    return NULL;
}
int inMapBounds(int x, int y)
{
    return ( (x >= 0 && x < MAP_WIDTH) && (y >= 0 && y < MAP_HEIGHT) );
}
int isWallAt(int x, int y)
{
    //printf("checking if wall at (%d, %d)\n", x, y);
    if(inMapBounds(x, y))
    {
        //printf("result: %d\n", map[y*MAP_WIDTH+x]);
        return walls[y][x] > 0;
    }
    return 1;
}
Texture* getFloorTexture(int x, int y)
{
    if(inMapBounds(x, y))
        return getTexture(floor[y][x]-1);
    return getTexture(TEXTURE_COBBLESTONE);
}
Texture* getCeilingTexture(int x, int y)
{
    if(inMapBounds(x, y))
        return getTexture(ceil[y][x]-1);
    return getTexture(TEXTURE_COBBLESTONE);
}
